package ansi

// Names and escape codes for the standard ANSI colors.
const (
	Black         = `30`
	BrightBlack   = `30;1`
	Red           = `31`
	BrightRed     = `31;1`
	Green         = `32`
	BrightGreen   = `32;1`
	Yellow        = `33`
	BrightYellow  = `33;1`
	Blue          = `34`
	BrightBlue    = `34;1`
	Magenta       = `35`
	BrightMagenta = `35;1`
	Cyan          = `36`
	BrightCyan    = `36;1`
	White         = `37`
	BrightWhite   = `37;1`

	Reset = `0`
)
